# 02/02_string/labs/path_split.py

# 1. 사용자로부터 윈도우 디렉토리 경로를 입력받습니다.
# 예시 입력: c:\\program files\\python
# * 참고: 윈도우 경로를 입력받을 때 Python 문자열에서는 '\\'로 입력해야 합니다.
# 실제 사용자 입력은 'c:\program files\python'처럼 보이지만,
# 코드로 처리할 때는 이스케이프 문자를 고려해야 합니다.
# 여기서는 사용자 입력이 그대로 문자열로 들어온다고 가정하고 처리합니다.
path_input = input("Path: ")

# 2. 경로 구분자 '\\' 또는 '/'를 기준으로 문자열을 분리합니다.
# 윈도우 경로는 '\\'를 사용하지만, 사용자 편의를 위해 '/'도 고려할 수 있습니다.
# 여기서는 예시를 따라 '\\'로 분리합니다.
# 주의: 이스케이프 문자 때문에 '\\' 대신 분리 시 '\\\\'를 사용하거나,
# 사용자 입력이 raw string (r'c:\...') 형태가 아니므로,
# 가장 안전하게 최종 디렉토리를 얻는 방법을 사용합니다.

# 3. 경로를 '\\'를 기준으로 분리하고 마지막 요소를 추출합니다.
# rsplit('\\', 1)[-1] 을 사용하여 안전하게 처리하거나,
# OS 모듈을 사용하는 것이 가장 좋으나, 문자열 처리 문제이므로 split()을 사용합니다.

# '\\'를 분리자로 사용합니다.
# Python 인터프리터에서 '\\'를 구분자로 쓰려면 '\\\\'로 작성해야 합니다.
path_parts = path_input.split('\\')

# 4. 분리된 리스트의 가장 마지막 요소를 최종 디렉토리 이름으로 선택합니다.
# 예: ['c:', 'program files', 'python'] -> 'python'
final_directory = path_parts[-1]

# 5. 결과를 출력합니다.
print(final_directory)

# 예시 입력: c:\program files\python
# 예시 출력: python