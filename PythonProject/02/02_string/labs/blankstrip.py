# 공백과 줄 바꿈 기호가 포함된 문자열 (양 끝에 공백이 있습니다.)
# 실제 코드에는 눈에 보이지 않는 줄 바꿈 기호(\n) 등도 포함될 수 있습니다.
code = '   000660   '

# 1. strip() 메서드를 사용하여 문자열의 양쪽 끝에 있는 공백 및
# 기타 공백 문자(줄 바꿈, 탭 등)를 제거합니다.
strip_code = code.strip()

# 2. 요구 사항에 맞게 결과 문자열을 출력합니다.
print(strip_code)

# 출력 결과:
# 000660